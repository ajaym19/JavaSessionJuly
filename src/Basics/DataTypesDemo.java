package Basics;

public class DataTypesDemo {

	/*
	 * JAVA is case sensitive language Data Types: Variable: is like a placeholder
	 * which can contain some values In variables we store data we have to define
	 * data type of each variable 2 types: datatypes 1. Primitive: byte, short, int,
	 * long, char, float, double, boolean 2. Non Primitive: String, Array
	 */
	public static void main(String[] args) {

		/*
		 * Integer Values: 45, 87, 102 byte, short, int, long Float values: 55.65, 45.98
		 * float, double single character: A, b, r, 5 char boolean values: true or false
		 * boolean
		 */

		byte b1 = 125;
		int i1 = 5656546; // -2^31 to 2^31
		System.out.println("b1"); // b1
		System.out.println(b1); // 45

		float f1 = 25.65f;
		double d1 = 4545.64566565676576;
		System.out.println(f1);

		// character
		char c1 = '&';
		System.out.println(c1);

		// boolean
		boolean ispresent = true;
		System.out.println(ispresent);

		/*
		 * Configure all
		 * Run a hello world java program
		 * create a program with all data types
		 * variables and print
		 */
		
	}
}
